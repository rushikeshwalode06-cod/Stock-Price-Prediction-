import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
sns.set_style('darkgrid')

data = pd.read_csv('stock_dataset_large.csv')

data

data.isnull().sum()

data = data.drop('Date',axis=1)

data

X = data.drop('Close',axis=1)

X

y = data.Close

y

from sklearn.model_selection import train_test_split

X_train,X_test,y_train,y_test = train_test_split(X,y,test_size=0.2,random_state=0)

X_train.shape

X_test.shape

from sklearn.linear_model import LinearRegression

model = LinearRegression()

model.fit(X_train,y_train)

y_test

y_pred = model.predict(X_test)

y_pred

from sklearn.metrics import r2_score

print(r2_score(y_test,y_pred))

def stock_price_prediction(Open,High,Low,volume):
    return model.predict([[Open,High,Low,volume]])

stock_price_prediction(200,210,195,500000)

stock_price_prediction(400,310,478,800000)

